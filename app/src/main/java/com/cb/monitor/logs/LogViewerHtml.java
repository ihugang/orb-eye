package com.cb.monitor.logs;

/**
 * Self-contained HTML for the /logs endpoint.
 *
 * Served as a single UTF-8 response — no external CSS/JS so the page works
 * even when the device is offline. Uses EventSource to subscribe to
 * /log-stream (SSE) for live tailing.
 */
public final class LogViewerHtml {

    private LogViewerHtml() {}

    public static String page() {
        return HTML;
    }

    private static final String HTML = ""
            + "<!doctype html>\n"
            + "<html lang=\"zh-CN\"><head><meta charset=\"utf-8\"/>\n"
            + "<meta name=\"viewport\" content=\"width=device-width,initial-scale=1\"/>\n"
            + "<title>orb-eye logs</title>\n"
            + "<style>\n"
            + "  :root { color-scheme: dark; }\n"
            + "  * { box-sizing: border-box; }\n"
            + "  html,body { height:100%; margin:0; }\n"
            + "  body { background:#0f1115; color:#d7dae0; font:13px/1.45 ui-monospace,SFMono-Regular,Menlo,Consolas,monospace; display:flex; flex-direction:column; }\n"
            + "  header { display:flex; flex-wrap:wrap; gap:8px 12px; align-items:center; padding:8px 12px; background:#181b22; border-bottom:1px solid #232832; }\n"
            + "  header h1 { margin:0 8px 0 0; font:600 14px/1 system-ui,-apple-system,Segoe UI,sans-serif; color:#e9edf3; letter-spacing:.3px; }\n"
            + "  header label { display:inline-flex; align-items:center; gap:4px; color:#9aa3b1; font-size:12px; cursor:pointer; user-select:none; }\n"
            + "  header input[type=text] { background:#0b0d12; border:1px solid #2a2f3a; color:#d7dae0; border-radius:4px; padding:3px 6px; width:140px; font:inherit; }\n"
            + "  header input[type=text]:focus { outline:none; border-color:#4e8cff; }\n"
            + "  header button { background:#232832; color:#d7dae0; border:1px solid #2a2f3a; border-radius:4px; padding:3px 10px; cursor:pointer; font:inherit; }\n"
            + "  header button:hover { background:#2c3340; }\n"
            + "  header button.primary { background:#2d6cdf; border-color:#2d6cdf; color:#fff; }\n"
            + "  header button.primary:hover { background:#3879ea; }\n"
            + "  #status { margin-left:auto; font-size:12px; color:#9aa3b1; }\n"
            + "  #status.on { color:#6fd38a; }\n"
            + "  #status.off { color:#e46868; }\n"
            + "  #log { flex:1; overflow:auto; padding:6px 0; }\n"
            + "  .row { padding:1px 12px; white-space:pre-wrap; word-break:break-word; display:flex; gap:8px; }\n"
            + "  .row:hover { background:#151923; }\n"
            + "  .row .ts { color:#6b7384; flex:0 0 72px; }\n"
            + "  .row .src { flex:0 0 52px; font-weight:600; }\n"
            + "  .row .src.engine { color:#7cb0ff; }\n"
            + "  .row .src.user { color:#6fd38a; }\n"
            + "  .row .exec { color:#b08cff; flex:0 0 auto; }\n"
            + "  .row.error .msg { color:#ff8f8f; }\n"
            + "  .row .msg { flex:1; min-width:0; }\n"
            + "  .row.hidden { display:none; }\n"
            + "</style></head><body>\n"
            + "<header>\n"
            + "  <h1>orb-eye 日志</h1>\n"
            + "  <label><input type=\"checkbox\" id=\"engine\" checked/> 引擎轨迹</label>\n"
            + "  <label>exec <input type=\"text\" id=\"execFilter\" placeholder=\"执行ID\"/></label>\n"
            + "  <label>过滤 <input type=\"text\" id=\"textFilter\" placeholder=\"关键字\"/></label>\n"
            + "  <label><input type=\"checkbox\" id=\"autoscroll\" checked/> 自动滚动</label>\n"
            + "  <button id=\"pause\">暂停</button>\n"
            + "  <button id=\"clear\">清空</button>\n"
            + "  <button id=\"reconnect\" class=\"primary\">重连</button>\n"
            + "  <span id=\"status\" class=\"off\">未连接</span>\n"
            + "</header>\n"
            + "<div id=\"log\"></div>\n"
            + "<script>\n"
            + "(function(){\n"
            + "  var MAX=5000;\n"
            + "  var es=null, paused=false, lastId=0;\n"
            + "  var $=function(id){return document.getElementById(id);};\n"
            + "  var logEl=$('log'), statusEl=$('status');\n"
            + "  var engineChk=$('engine'), execInp=$('execFilter'), textInp=$('textFilter'), autoChk=$('autoscroll');\n"
            + "  function setStatus(txt, on){ statusEl.textContent=txt; statusEl.className=on?'on':'off'; }\n"
            + "  function fmtTs(ms){ var d=new Date(ms); function p(n){return n<10?'0'+n:n;} return p(d.getHours())+':'+p(d.getMinutes())+':'+p(d.getSeconds())+'.'+String(d.getMilliseconds()).padStart(3,'0'); }\n"
            + "  function matches(row){\n"
            + "    if(row.dataset.src==='engine' && !engineChk.checked) return false;\n"
            + "    var ef=execInp.value.trim(); if(ef && row.dataset.exec!==ef) return false;\n"
            + "    var tf=textInp.value.trim().toLowerCase(); if(tf && row.dataset.msg.toLowerCase().indexOf(tf)===-1) return false;\n"
            + "    return true;\n"
            + "  }\n"
            + "  function applyFilters(){ var rows=logEl.children; for(var i=0;i<rows.length;i++){ rows[i].classList.toggle('hidden', !matches(rows[i])); } }\n"
            + "  function append(entry){\n"
            + "    if(entry.id>lastId) lastId=entry.id;\n"
            + "    var row=document.createElement('div');\n"
            + "    row.className='row'+(entry.level==='error'?' error':'');\n"
            + "    row.dataset.src=entry.source; row.dataset.exec=entry.execId||''; row.dataset.msg=entry.msg||'';\n"
            + "    var ts=document.createElement('span'); ts.className='ts'; ts.textContent=fmtTs(entry.ts); row.appendChild(ts);\n"
            + "    var src=document.createElement('span'); src.className='src '+entry.source; src.textContent=entry.source; row.appendChild(src);\n"
            + "    if(entry.execId){ var ex=document.createElement('span'); ex.className='exec'; ex.textContent='#'+entry.execId; row.appendChild(ex); }\n"
            + "    var msg=document.createElement('span'); msg.className='msg'; msg.textContent=entry.msg||''; row.appendChild(msg);\n"
            + "    if(!matches(row)) row.classList.add('hidden');\n"
            + "    logEl.appendChild(row);\n"
            + "    while(logEl.childElementCount>MAX) logEl.removeChild(logEl.firstChild);\n"
            + "    if(autoChk.checked && !paused){ logEl.scrollTop=logEl.scrollHeight; }\n"
            + "  }\n"
            + "  function connect(){\n"
            + "    if(es){ try{es.close();}catch(e){} }\n"
            + "    setStatus('连接中...', false);\n"
            + "    es=new EventSource('/log-stream');\n"
            + "    es.onopen=function(){ setStatus('已连接', true); };\n"
            + "    es.onerror=function(){ setStatus('连接断开,自动重连中', false); };\n"
            + "    es.onmessage=function(ev){ if(paused) return; try{ append(JSON.parse(ev.data)); }catch(e){} };\n"
            + "  }\n"
            + "  $('clear').onclick=function(){ logEl.innerHTML=''; };\n"
            + "  $('reconnect').onclick=function(){ lastId=0; logEl.innerHTML=''; connect(); };\n"
            + "  $('pause').onclick=function(){ paused=!paused; this.textContent=paused?'继续':'暂停'; };\n"
            + "  engineChk.onchange=applyFilters; execInp.oninput=applyFilters; textInp.oninput=applyFilters;\n"
            + "  connect();\n"
            + "})();\n"
            + "</script></body></html>\n";
}
